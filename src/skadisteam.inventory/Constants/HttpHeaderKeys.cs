namespace skadisteam.inventory.Constants
{
    /// <summary>
    /// Class which includes all header keys
    /// for HTTP requests.
    /// </summary>
    internal static class HttpHeaderKeys
    {
        /// <summary>
        /// HTTP Header Key for the Accept Header.
        /// </summary>
        internal const string Accept = "Accept";

        /// <summary>
        /// HTTP Header Key for the Accept-Encoding Header.
        /// </summary>
        internal const string AcceptEncoding = "Accept-Encoding";

        /// <summary>
        /// HTTP Header Key for the Accept-Language Header.
        /// </summary>
        internal const string AcceptLanguage = "Accept-Language";

        /// <summary>
        /// HTTP Header Key for the Cache-Control Header.
        /// </summary>
        internal const string CacheControl = "Cache-Control";

        /// <summary>
        /// HTTP Header Key for the Pragma Header.
        /// </summary>
        internal const string Pragma = "Pragma";

        /// <summary>
        /// HTTP Header Key for the Referer Header.
        /// </summary>
        internal const string Referer = "Referer";

        /// <summary>
        /// HTTP Header Key for the Upgrade-Insecure-Requests Header.
        /// </summary>
        internal const string UpgradeInsecureRequests =
            "Upgrade-Insecure-Requests";

        /// <summary>
        /// HTTP Header Key for the User-Agent Header.
        /// </summary>
        internal const string UserAgent = "User-Agent";

        /// <summary>
        /// HTTP Header Key for the X-Prototype-Version Header.
        /// </summary>
        internal const string XPrototypeVersion = "X-Prototype-Version";

        /// <summary>
        /// HTTP Header Key for the X-Requested-With Header.
        /// </summary>
        internal const string XRequestedWith = "X-Requested-With";
    }
}
